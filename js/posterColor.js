
function updateBorders_datetitle(color,obj2,obj3,obj4,obj5) {//导航
    var hexColor = "transparent";
    if(color) {
       var hsv = color.toHsv();
       var range=hsv.a;  //获取透明度
       var hexColor = color.toRgbString(); //获取rgb
       hexColor= hexColor.replace(/[^0-9,]/g, '');
       var rgb1 = hexColor.split(",")[0];
       var rgb2 = hexColor.split(",")[1];
       var rgb3 = hexColor.split(",")[2];
       obj2.css("color","rgb("+rgb1+","+rgb2+","+rgb3+")");
       obj3.css("border-color","rgb("+rgb1+","+rgb2+","+rgb3+")");
       obj4.css("border-bottom-color","rgb("+rgb1+","+rgb2+","+rgb3+")");
       obj5.css("color","rgb("+rgb1+","+rgb2+","+rgb3+")");
    }
}
